/**
 * @file    spi_w6100.h
 * @brief   W6100 SPI 底层驱动（基于 bsp_spi）
 *
 * ============================================================================
 *  引脚映射（使用 SPI1 默认引脚）
 * ============================================================================
 *
 *   SCK  - PA5  (AF5)
 *   MISO - PA6  (AF5)
 *   MOSI - PA7  (AF5)
 *   CS   - PA4  （软件 GPIO，推挽输出）
 *   RST  - PC6  （推挽输出）
 *
 *   如需自定义 SPI 引脚，在调用 w6100_spi_init 前修改 me->sck_port 等字段。
 *
 * ============================================================================
 *  SPI 时序
 * ============================================================================
 *
 *   MODE0（CPOL=0, CPHA=0），MSB 先行
 *   默认分频 32（APB2=100MHz => 3.125MHz），可通过 baud_prescaler 调整
 */

#ifndef SPI_W6100_H
#define SPI_W6100_H

#include <stdint.h>

struct bsp_spi;

/* ============================================================================
 * W6100 SPI 初始化
 * ============================================================================ */

/**
 * @brief  初始化 SPI1 外设并绑定 W6100
 *
 * 完成以下工作：
 *   1. 初始化 bsp_spi（SPI1, PA5/PA6/PA7, PA4 CS, MODE0, 3.125MHz）
 *   2. 配置 PC6 为 RST 输出（高电平）
 *
 * @param me    空闲的 bsp_spi 对象指针（传 NULL 则内部使用静态实例）
 *
 * @return 0  成功
 * @return -1 bsp_spi_init 失败
 */
int  w6100_spi_init(struct bsp_spi *me);

/**
 * @brief  W6100 硬件复位（PC6 低→高）
 */
void w6100_hw_reset(void);

/* ============================================================================
 * ioLibrary 回调函数
 *
 * 这些函数通过 reg_wizchip_cs_cbfunc / reg_wizchip_spi_cbfunc 注册到 ioLibrary。
 * w6100_cs_sel / w6100_cs_desel 控制 PA4 片选。
 * ============================================================================ */

void w6100_cs_sel(void);
void w6100_cs_desel(void);

uint8_t w6100_spi_read_byte(void);
void    w6100_spi_write_byte(uint8_t tx);
void    w6100_spi_read_burst(uint8_t *buf, int16_t len);
void    w6100_spi_write_burst(uint8_t *buf, int16_t len);

/* ============================================================================
 * 单事务 SPI 传输（地址+数据在一次 HAL 调用中完成，无 SCK 间隙）
 * ============================================================================ */
int w6100_hal_xfer(uint8_t *tx, uint8_t *rx, uint16_t len);

/* ============================================================================
 * 直接寄存器 SPI 访问（绕过 HAL，用于诊断对比）
 * ============================================================================ */
uint8_t w6100_spi_direct_rw(uint8_t data);
void    w6100_cs_direct_sel(void);
void    w6100_cs_direct_desel(void);

#endif /* SPI_W6100_H */
