/**
 ****************************************************************************************************
 * @file        bsp_adc.h
 * @brief       BSP — RTC 实时时钟 & 内置温度传感器驱动（F407 版）
 * @note        RTC 使用 LSE（32.768 kHz），日历 BCD 格式
 *              温度传感器使用 ADC1 通道 16（ADC1_IN16）
 *              VBAT 使用 ADC1 通道 18（ADC1_IN18），分压比 1/2
 ****************************************************************************************************
 */

#ifndef BSP_ADC_H
#define BSP_ADC_H

#include <stdint.h>

/* ==========================================================================
 * RTC 实时时钟
 * ========================================================================== */

/**
 * @brief  初始化 RTC（LSE 时钟源，设置默认日历）
 * @note   首次运行或备份域复位后设置初始时间 2026-07-01 00:00:00（周三）
 *         VBAT 模式下保持运行，后续复位不会重置日历
 */
void rtc_init(void);

/**
 * @brief  读取当前时间（时、分、秒）
 * @param  hour     小时（BCD，24h 制 0~23）
 * @param  minute   分钟（BCD，0~59）
 * @param  second   秒（BCD，0~59）
 */
void rtc_get_time(uint8_t *hour, uint8_t *minute, uint8_t *second);

/**
 * @brief  读取当前日期（年、月、日、星期）
 * @param  year     年（BCD，0~99，基准 2000）
 * @param  month    月（BCD，1~12）
 * @param  day      日（BCD，1~31）
 * @param  weekday  星期（1=周一 ~ 7=周日）
 */
void rtc_get_date(uint8_t *year, uint8_t *month, uint8_t *day, uint8_t *weekday);

/**
 * @brief  获取时间字符串 "HH:MM:SS"
 * @param  buf  输出缓冲区（至少 9 字节）
 */
void rtc_get_time_str(char *buf);

/**
 * @brief  获取日期字符串 "YYYY-MM-DD W"
 * @param  buf  输出缓冲区（至少 16 字节）
 */
void rtc_get_date_str(char *buf);

/* ==========================================================================
 * 内置温度传感器
 * ========================================================================== */

/**
 * @brief  读取芯片结温
 * @retval 温度值，单位 °C（放大 100 倍，如 2550 = 25.50°C）
 * @note   首次调用会自动初始化 ADC1
 *         校准值 TS_CAL1（30°C）@ 0x1FFF7A2C
 *                  TS_CAL2（110°C）@ 0x1FFF7A2E
 */
int32_t chip_temp_read(void);

/* ==========================================================================
 * VBAT 电压监测（纽扣电池）
 * ========================================================================== */

/**
 * @brief  读取 VBAT 引脚电压（纽扣电池开路电压）
 * @retval 电压值，单位 mV（如 3000 = 3.000 V）
 * @note   使用 ADC1 内部通道 VBAT（通道 18），分压比 1/2
 *         首次调用会自动初始化 ADC1
 *         需 VDD 供电时测量，反映电池开路电压，低于 2400 mV 建议更换
 */
int32_t chip_vbat_read(void);

#endif /* BSP_ADC_H */
